public class Book {
String name;
int publication;

    public Book(String name, int publication) {
        //Book book = new Book();
        this.name = name;
        this. publication = publication;

    }

    public String getName() {
        return this.name;
    }

    public int getPublication() {
        return this.publication;
    }

    public void setPublication(int publication) {
        this.publication = publication;

    }


}
